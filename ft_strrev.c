#include "libft.h"

char		*ft_strrev(char *str)
{
	int		i;
	int		l;

	l = 0;
	while (str[l] != '\0')
		l++;
	i = -1;
	while (++i < --l)
		ft_swap(str + i, str + l);
	return (str);
}
